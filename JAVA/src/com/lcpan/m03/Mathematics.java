package com.lcpan.m03;

public class Mathematics {
	public static void main(String[] args) {
		String name = "Peter" + ", " + "Pan";
		String kickname = "no. " + 1;
		System.out.println(name);
		System.out.println(kickname);
		System.out.println(9/4);
		System.out.println(9.0/4);
		System.out.println(9%4);
		System.out.println(9.0%4);
		int sec = 340;
		System.out.println(sec/60 + "分" + sec%60 + "秒");
	}
}
